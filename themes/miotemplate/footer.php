    <script>
    document.addEventListener('DOMContentLoaded', function() {
        const cartItems = document.querySelectorAll('.ilconteggioPCM');
        const totalElement = document.getElementById('totalPCM');

        // Nessun riferimento a subtotale o spedizione in questo script.

        /**
         * Aggiorna il riepilogo totale del carrello sommando i prezzi dei prodotti.
         * Tutte le operazioni numeriche avvengono in centesimi per garantire la precisione.
         */
        function updateSummary() {
            let currentItemsTotalCents = 0; // Inizializza il totale degli articoli in centesimi

            cartItems.forEach(item => {
                // Preleva il prezzo base dal dataset e convertilo in centesimi, arrotondando
                const priceCents = Math.round(parseFloat(item.dataset.price) * 100);

                const quantityInput = item.querySelector('.quantity-input');
                const quantity = parseInt(quantityInput.value);

                // Aggiungi al totale la quantità dell'articolo moltiplicata per il prezzo in centesimi
                currentItemsTotalCents += priceCents * quantity;
            });

            // Converti il totale dai centesimi in valuta (dollari/euro) per la visualizzazione
            const finalTotal = currentItemsTotalCents / 100;

            // Aggiorna l'elemento del totale nella pagina, formattandolo a due decimali
            if (totalElement) {
                totalElement.textContent = `$${finalTotal.toFixed(2)}`;
            }
        }

        /**
         * Itera su ogni singolo articolo nel carrello per configurare i listener per quantità.
         */
        cartItems.forEach(item => {
            const incrementButton = item.querySelector('.increment');
            const decrementButton = item.querySelector('.decrement');
            const quantityInput = item.querySelector('.quantity-input');
            const itemPriceElement = item.querySelector('.item-pricePCM');

            // Prezzo base dell'articolo convertito in centesimi per calcoli precisi
            const basePriceCents = Math.round(parseFloat(item.dataset.price) * 100);

            // Listener per il pulsante di incremento
            incrementButton.addEventListener('click', function() {
                let quantity = parseInt(quantityInput.value);
                quantity++;
                quantityInput.value = quantity;

                // Aggiorna il prezzo del singolo articolo, convertendo da centesimi per la visualizzazione
                itemPriceElement.textContent = `$${(basePriceCents * quantity / 100).toFixed(2)}`;
                updateSummary(); // Ricalcola il totale generale
            });

            // Listener per il pulsante di decremento
            decrementButton.addEventListener('click', function() {
                let quantity = parseInt(quantityInput.value);
                // Evita che la quantità scenda sotto il valore 'min' (solitamente 1)
                if (quantity > parseInt(quantityInput.getAttribute('min'))) {
                    quantity--;
                    quantityInput.value = quantity;

                    // Aggiorna il prezzo del singolo articolo
                    itemPriceElement.textContent = `$${(basePriceCents * quantity / 100).toFixed(2)}`;
                    updateSummary(); // Ricalcola il totale generale
                }
            });

            // Listener per la modifica diretta dell'input quantità
            quantityInput.addEventListener('change', function() {
                let quantity = parseInt(this.value);
                const min = parseInt(this.getAttribute('min')) || 1;
                // Assicurati che la quantità sia un numero valido e non inferiore al minimo
                if (isNaN(quantity) || quantity < min) {
                    this.value = min;
                    quantity = min;
                }

                // Aggiorna il prezzo del singolo articolo
                itemPriceElement.textContent = `$${(basePriceCents * quantity / 100).toFixed(2)}`;
                updateSummary(); // Ricalcola il totale generale
            });
        });

        // Esegui la funzione di riepilogo al caricamento iniziale della pagina
        // Questo assicura che il totale sia corretto fin dall'inizio, prima di qualsiasi interazione.
        updateSummary();
    });
</script>


