<!--utile per scelta brand pag shop-->
<script>

document.addEventListener('DOMContentLoaded', function() {
    const brandCheckboxes = document.querySelectorAll('input[name="product_brand[]"]');

    if (brandCheckboxes.length > 0) {
        brandCheckboxes.forEach(function(checkbox) {
            checkbox.addEventListener('change', function() {
                const form = this.closest('form');
                if (form) {
                    // Crea un nuovo URL con i parametri del form
                    const formData = new FormData(form);
                    const params = new URLSearchParams();

                    // Costruisci il nuovo URL escludendo i simboli problematici
                    formData.forEach((value, key) => {
                        const newKey = key.replace(/%5B%5D|\[\]/g, ''); // Rimuovi [] e la sua codifica
                        params.append(newKey, value);
                    });

                    // Reindirizza l'utente al nuovo URL pulito
                    window.location.href = form.action + '?' + params.toString();
                }
            });
        });
    }
});

</script>



<!--utile per il fitro categorie in shop-->
<script>

document.addEventListener('DOMContentLoaded', function() {
    const categoryCheckboxes = document.querySelectorAll('input[name="product_cat[]"]');

    if (categoryCheckboxes.length > 0) {
        categoryCheckboxes.forEach(function(checkbox) {
            checkbox.addEventListener('change', function() {
                const form = this.closest('form');
                if (form) {
                    const params = new URLSearchParams(window.location.search);

                    // Rimuove tutti i parametri di categoria esistenti
                    params.delete('product_cat');

                    // Aggiunge i parametri solo per i checkbox selezionati
                    document.querySelectorAll('input[name="product_cat[]"]:checked').forEach(function(checkedBox) {
                        params.append('product_cat', checkedBox.value);
                    });

                    // Ottiene l'URL base e aggiunge i nuovi parametri
                    const formAction = form.action.split('?')[0];
                    window.location.href = formAction + '?' + params.toString();
                }
            });
        });
    }
});

</script>


